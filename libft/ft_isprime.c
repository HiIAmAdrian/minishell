#include "./includes/libft.h"

int	ft_isprime(size_t n)
{
	size_t	i;
	int		k;

	i = 2;
	k = 1;
	while (i * i <= n && k == 1)
	{
		if (n % i == 0)
			k = 0;
		i++;
	}
	return (k);
}
