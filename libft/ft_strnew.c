#include <stdlib.h>

char	*ft_strnew(size_t size)
{
	char	*s;
	size_t	i;

	i = 0;
	if ((s = (char *)malloc(size + 1)))
	{
		while (i <= size)
		{
			s[i] = 0;
			i++;
		}
		return (s);
	}
	else
		return (NULL);
}
